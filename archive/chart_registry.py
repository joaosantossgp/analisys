# ── Helpers ──────────────────────────────────────────────────────────────────

def get_section_charts(section: str) -> dict[str, ChartSpec]:
    """Return all charts in a given section."""
    return {k: v for k, v in CHART_REGISTRY.items() if v.get("section") == section}


def get_chart_spec(key: str) -> ChartSpec | None:
    """Return a single chart spec by key."""
    return CHART_REGISTRY.get(key)
