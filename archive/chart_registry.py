"""
dashboard/chart_registry.py — Declarative registry mapping KPI keys to chart configurations.

Each entry defines the display name, section, KPI key, chart type, granularity, unit,
human-readable formula, optional reference lines, and color key.
"""

from typing import TypedDict, Any


class ChartSpec(TypedDict, total=False):
    name: str          # Display name
    section: str       # Section name (Liquidez, Rentabilidade, etc.)
    kpi_key: str       # Key in kpis dict
    chart_type: str    # line_markers | vertical_bars | grouped_bars | stacked_100 | combo_bar_line | waterfall | funnel_lines | indexed_lines | horizontal_bars | radar | scatter | sankey
    granularity: str   # annual | quarterly | both
    unit: str          # % | R$ mi | x | dias | R$ bi
    formula: str       # Human readable formula
    ref_lines: list    # [{"value": 1.0, "label": "Referência", "style": "equilibrio"}]
    color_key: str     # Key into COLORS_PLAN
    defaults: dict     # Default filter values


